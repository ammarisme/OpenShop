         * Login a User and check his role. 
         * If the user is a Customer -> redirect to Customer Area
         * If the user is a Retailer -> redirect to Retailer Area
         * If the user is a Wholesale-> redirect to Wholesale Area
         */
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginViewModel model, string returnUrl)
        {
            if (ModelState.IsValid)
            {
                var user = await UManager.FindAsync(model.Email, model.Password);
                if (user != null)
                {
                    await SignInAsync(user, model.RememberMe);

                    // check what is the role of the user

                    var isRetailer = await UManager.IsInRoleAsync(user.Id, "Retailer");
                    
                    if (isRetailer)
                    {
                        return RedirectToAction("Dashboard", "MyAccount", new { area = "Account" });
                    }
                    

                    //return RedirectToLocal(returnUrl);
                }
                else
                {
                    ModelState.AddModelError("", "Invalid username or password.");
                }
            }

            // If we got this far, something failed, redisplay form
            return View(model);
        }

        private async Task SignInAsync(ApplicationUser user, bool isPersistent)
        {
            AuthenticationManager.SignOut(DefaultAuthenticationTypes.ExternalCookie);
            var identity = await UManager.CreateIdentityAsync(user, DefaultAuthenticationTypes.ApplicationCookie);
            // Add more custom claims here if you want. Eg HomeTown can be a claim for the User
            AuthenticationManager.SignIn(new AuthenticationProperties() { IsPersistent = isPersistent }, identity);
        }


